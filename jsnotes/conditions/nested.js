let restaurant = 'Chick-fil-A',
    order = 'Spicy Chicken Sandwich',
    price = 0;

if (restaurant == 'Chick-fil-A') {
  if (order == '8-piece Nuggets') {
    price = 3.85;
  } else if (order == 'Spicy Chicken Sandwich') {
    price = 3.99;
  }
} else if (restaurant == 'In-N-Out') {
  if (order == 'Hamburger') {
    price = 2.10;
  } else if (order == 'Cheeseburger') {
    price = 2.40;
  }
}
